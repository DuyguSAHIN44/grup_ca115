package day06_7Nisan_FoorLoop;

public class C02_FoorLoop {
    public static void main(String[] args) {


        // 50'den baslayip, 100'e kadar(100 dahil) sayilari yan yana,
        // aralarinda bir bosluk olacak sekilde yazdirin

        for (int i = 50; i <=100; i++) {
            System.out.print(i+" ");

        }
    }
}
